// コンストラクタ
YM2151::YM2151(device_t* device, int clock, int rate)
    : device(device), clock(clock), volume(16384) { // 16384 = 1.0倍
    
    ::memset(oper, 0, sizeof(oper));
    ::memset(regs, 0, sizeof(regs));
    ::memset(chanout, 0, sizeof(chanout));
    ::memset(pan, 0, sizeof(pan));

    this->sampfreq = rate ? rate : 44100;
    this->irqhandler = nullptr;
    porthandler = nullptr;

    // 完全に整数だけで時間増分を算出
    lfo_timer_add = (uint32_t)(((uint64_t)(1 << LFO_SH) * clock) / (64 * this->sampfreq));
    eg_timer_add  = (uint32_t)(((uint64_t)(1 << EG_SH) * clock) / (64 * this->sampfreq));
    eg_timer_overflow = 3 * (1 << EG_SH);

    tim_A = 0;
    tim_B = 0;
}

// クロックに依存するテーブルのみ実行時に整数演算で生成
void YM2151::initChipTables() {
    for (int i = 0; i < 1024; i++) {
        freq_array[i] = (uint32_t)(((uint64_t)i * 268435456) / clock);
        tim_A_tab[i] = (1024 - i) << (TIMER_SH - 10);
    }
    for (int i = 0; i < 256; i++) {
        tim_B_tab[i] = (256 - i) << (TIMER_SH - 8);
    }
    for (int i = 0; i < 32; i++) {
        noise_tab[i] = i ? (uint32_t)(clock / (i * 128)) : 0;
    }
}

void YM2151::resetChip() {
    for (int i = 0; i < 32; i++) {
        ::memset(&oper[i], 0, sizeof(YM2151Operator));
        oper[i].state = EG_OFF;
        oper[i].volume = MAX_ATT_INDEX;
        oper[i].kc_i = 768;
    }

    ::memset(regs, 0, sizeof(regs));
    eg_timer = 0; eg_cnt = 0; lfo_timer = 0; lfo_counter = 0;
    lfo_phase = 0; lfo_wsel = 0; pmd = 0; amd = 0; lfa = 0; lfp = 0;
    test = 0; irq_enable = 0; status = 0; tim_A = 0; tim_B = 0;
    tim_A_val = 0; tim_B_val = 0; timer_A_index = 0; timer_B_index = 0;
    noise = 0; noise_rng = 1; noise_p = 0; noise_f = noise_tab[0];
    csm_req = 0;
}

void YM2151::keyOn(YM2151Operator* op, uint32_t bit) {
    if (op->state == EG_OFF || op->state == EG_REL) {
        op->phase = 0;
        op->state = EG_ATT;
    }
}

void YM2151::keyOff(YM2151Operator* op, uint32_t bit) {
    if (op->state != EG_OFF) {
        op->state = EG_REL;
    }
}

void YM2151::advanceEg() {
    eg_timer += eg_timer_add;
    while (eg_timer >= eg_timer_overflow) {
        eg_timer -= eg_timer_overflow;
        eg_cnt++;

        for (int i = 0; i < 32; i++) {
            YM2151Operator* op = &oper[i];
            switch (op->state) {
            case EG_ATT:
                if (op->volume > 0) op->volume -= 2;
                else op->state = EG_DEC;
                break;
            case EG_DEC:
                if (op->volume < op->d1l) op->volume += 1;
                else op->state = EG_SUS;
                break;
            case EG_SUS:
                if (op->volume < MAX_ATT_INDEX) op->volume += 1;
                break;
            case EG_REL:
                if (op->volume < MAX_ATT_INDEX) op->volume += 4;
                else op->state = EG_OFF;
                break;
            case EG_OFF:
            default:
                op->volume = MAX_ATT_INDEX;
                break;
            }
        }
    }
}

void YM2151::advanceLfoAndPhase() {
    lfo_timer += lfo_timer_add;
    if (lfo_timer >= (1 << LFO_SH)) {
        lfo_timer -= (1 << LFO_SH);
        lfo_counter += lfo_counter_add;
        lfo_phase = (lfo_counter >> 4) & 0xff;

        switch (lfo_wsel) {
        case 0: lfa = lfo_phase; break;
        case 1: lfa = (lfo_phase < 128) ? (lfo_phase * 2) : (511 - lfo_phase * 2); break;
        case 2: lfa = 255; break;
        case 3: noise_rng ^= (noise_rng >> 7); break;
        }
        lfp = lfa;
    }

    for (int i = 0; i < 32; i++) {
        oper[i].phase += oper[i].freq;
    }
}

void YM2151::chanCalc(int ch) {
    YM2151Operator* op = &oper[ch * 4];
    int32_t env, phase;

    // M1
    env = op[0].volume + op[0].tl;
    if (env < MAX_ATT_INDEX) {
        phase = (op[0].phase >> 16) & 0x1ff;
        op_out[0] = sin_tab[phase] * tl_tab[env];
    } else op_out[0] = 0;

    // C1
    env = op[1].volume + op[1].tl;
    if (env < MAX_ATT_INDEX) {
        phase = ((op[1].phase + (op_out[0] >> 1)) >> 16) & 0x1ff;
        op_out[1] = sin_tab[phase] * tl_tab[env];
    } else op_out[1] = 0;

    // M2
    env = op[2].volume + op[2].tl;
    if (env < MAX_ATT_INDEX) {
        phase = ((op[2].phase + (op_out[1] >> 1)) >> 16) & 0x1ff;
        op_out[2] = sin_tab[phase] * tl_tab[env];
    } else op_out[2] = 0;

    // C2
    env = op[3].volume + op[3].tl;
    if (env < MAX_ATT_INDEX) {
        phase = ((op[3].phase + (op_out[2] >> 1)) >> 16) & 0x1ff;
        chanout[ch] = (sin_tab[phase] * tl_tab[env]) >> 14;
    } else chanout[ch] = 0;
}

void YM2151::chan7Calc() {
    chanCalc(7);
}

void YM2151::updateOne(int16_t* buffer, int length) {
    if (!tables_initialized) {
        initChipTables();
        resetChip();
        tables_initialized = 1;
    }

    int32_t outl = 0, outr = 0;

    for (int i = 0; i < length; i++) {
        advanceEg();
        ::memset(chanout, 0, sizeof(chanout));

        for (int ch = 0; ch < 7; ch++) chanCalc(ch);
        chan7Calc();

        outl = 0; outr = 0;
        for (int ch = 0; ch < 8; ch++) {
            outl += (chanout[ch] & pan[ch * 2]);
            outr += (chanout[ch] & pan[ch * 2 + 1]);
        }

        outl >>= FINAL_SH; outr >>= FINAL_SH;
        if (outl > MAXOUT) outl = MAXOUT; else if (outl < MINOUT) outl = MINOUT;
        if (outr > MAXOUT) outr = MAXOUT; else if (outr < MINOUT) outr = MINOUT;

        // ボリュームも完全整数演算（volume=16384が1.0倍なので、そのまま掛けて14シフト）
        *buffer++ = (int16_t)((outl * this->volume) >> 14);
        *buffer++ = (int16_t)((outr * this->volume) >> 14);

        if (tim_A) {
            tim_A_val -= (1 << TIMER_SH);
            if (tim_A_val <= 0) {
                tim_A_val += tim_A_tab[timer_A_index];
                if ((irq_enable & 0x04) && irqhandler) irqhandler(device, 1);
            }
        }
        if (tim_B) {
            tim_B_val -= (1 << TIMER_SH);
            if (tim_B_val <= 0) {
                tim_B_val += tim_B_tab[timer_B_index];
                if ((irq_enable & 0x08) && irqhandler) irqhandler(device, 1);
            }
        }
        advanceLfoAndPhase();
    }
}

void YM2151::writeReg(uint8_t reg, uint8_t data) {
    regs[reg] = data;

    switch (reg) {
    case 0x01: test = data; break;
    case 0x08: {
        unsigned int ch = data & 7;
        YM2151Operator* op = &oper[ch * 4];
        if (data & 0x08) keyOn(&op[0], 1);  else keyOff(&op[0], ~1);
        if (data & 0x20) keyOn(&op[1], 1);  else keyOff(&op[1], ~1);
        if (data & 0x10) keyOn(&op[2], 1);  else keyOff(&op[2], ~1);
        if (data & 0x40) keyOn(&op[3], 1);  else keyOff(&op[3], ~1);
        break;
    }
    case 0x0f: noise = data; noise_f = noise_tab[data & 0x1f]; break;
    case 0x10: timer_A_index = (timer_A_index & 0x03) | ((uint32_t)data << 2); break;
    case 0x11: timer_A_index = (timer_A_index & 0x3fc) | (data & 0x03); break;
    case 0x12: timer_B_index = data; break;
    case 0x14:
        irq_enable = data & 0xbc;
        if (data & 0x10) { status &= ~1; if (irqhandler) irqhandler(device, 0); }
        if (data & 0x20) { status &= ~2; if (irqhandler) irqhandler(device, 0); }
        if (data & 0x01) { if (!tim_A) { tim_A_val = tim_A_tab[timer_A_index]; tim_A = 1; } } else tim_A = 0;
        if (data & 0x02) { if (!tim_B) { tim_B_val = tim_B_tab[timer_B_index]; tim_B = 1; } } else tim_B = 0;
        break;
    case 0x18: lfo_counter_add = data; break;
    case 0x19: if (data & 0x80) pmd = data & 0x7f; else amd = data & 0x7f; break;
    case 0x1b: lfo_wsel = data & 3; if (porthandler) porthandler(device, 0, data >> 6); break;
    default:
        if (reg >= 0x20 && reg <= 0x3f) {
            unsigned int ch = reg & 7;
            YM2151Operator* op = &oper[ch * 4];
            if (reg >= 0x20 && reg <= 0x27) {
                pan[ch * 2]     = (data & 0x80) ? 0xffffffff : 0;
                pan[ch * 2 + 1] = (data & 0x40) ? 0xffffffff : 0;
                uint8_t fb = (data >> 3) & 7;
                op[0].fb_shift = fb ? fb + 5 : 0;
            } else if (reg >= 0x28 && reg <= 0x2f) refreshRegisters();
            else if (reg >= 0x30 && reg <= 0x37) refreshRegisters();
            else if (reg >= 0x38 && reg <= 0x3f) {
                for (int i = 0; i < 4; i++) { op[i].pms = (data >> 4) & 7; op[i].ams = data & 3; }
            }
        } else if (reg >= 0x40 && reg <= 0xff) {
            unsigned int op_idx = ((reg & 7) * 4) + ((reg >> 3) & 3);
            YM2151Operator* op = &oper[op_idx];
            if (reg >= 0x40 && reg <= 0x5f) {
                op->dt1 = (data >> 4) & 7; op->mul = data & 0x0f; if (!op->mul) op->mul = 1;
                refreshRegisters();
            } else if (reg >= 0x60 && reg <= 0x7f) op->tl = data & 0x7f;
            else if (reg >= 0x80 && reg <= 0x9f) {
                uint8_t ar = data & 0x1f; op->eg_sh_ar = ar ? 15 - (ar >> 2) : 0; op->eg_sel_ar = (ar & 3) * 8;
            } else if (reg >= 0xa0 && reg <= 0xbf) {
                uint8_t d1r = data & 0x1f; op->eg_sh_d1r = d1r ? 15 - (d1r >> 2) : 0; op->eg_sel_d1r = (d1r & 3) * 8;
                op->AMmask = (data & 0x80) ? 0xffffffff : 0;
            } else if (reg >= 0xc0 && reg <= 0xdf) {
                uint8_t d2r = data & 0x1f; op->eg_sh_d2r = d2r ? 15 - (d2r >> 2) : 0; op->eg_sel_d2r = (d2r & 3) * 8;
                op->dt2 = (data >> 6) & 3; refreshRegisters();
            } else if (reg >= 0xe0 && reg <= 0xff) {
                uint8_t rr = data & 0x0f; op->eg_sh_rr = rr ? 15 - (rr >> 2) : 0; op->eg_sel_rr = (rr & 3) * 8;
                uint32_t d1l_val = (data >> 4) & 0x0f; op->d1l = d1l_val ? d1l_val << 5 : MAX_ATT_INDEX;
            }
        }
        break;
    }
}

void YM2151::refreshRegisters() {
    for (unsigned int ch = 0; ch < 8; ch++) {
        uint32_t kc = regs[0x28 + ch];
        uint32_t kf = regs[0x30 + ch];
        uint32_t kc_i = (kc & 0x7f) * 8 + (kf >> 5);
        for (unsigned int i = 0; i < 4; i++) {
            YM2151Operator* op = &oper[ch * 4 + i];
            op->kc_i = kc_i;
            uint32_t channel_pitch = kc_i + op->dt2;
            if (channel_pitch >= 1024) channel_pitch = 1023;
            op->freq = ((freq_array[channel_pitch] + op->dt1) * op->mul) >> 1;
        }
    }
}
